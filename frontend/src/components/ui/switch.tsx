'use client'

import * as React from 'react'
import { cn } from '@/lib/utils'

type SwitchProps = {
  checked?: boolean
  defaultChecked?: boolean
  onCheckedChange?: (checked: boolean) => void
} & React.ButtonHTMLAttributes<HTMLButtonElement>

const Switch = React.forwardRef<HTMLButtonElement, SwitchProps>(({ checked, defaultChecked, onCheckedChange, className, ...props }, ref) => {
  const [internalChecked, setInternalChecked] = React.useState<boolean>(Boolean(defaultChecked))
  const isControlled = checked !== undefined
  const value = isControlled ? Boolean(checked) : internalChecked

  return (
    <button
      ref={ref}
      type="button"
      role="switch"
      aria-checked={value}
      className={cn(
        'relative inline-flex h-6 w-11 shrink-0 cursor-pointer items-center rounded-full border-2 border-transparent transition-colors focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 focus-visible:ring-offset-background disabled:cursor-not-allowed disabled:opacity-50',
        value ? 'bg-primary' : 'bg-input',
        className
      )}
      onClick={() => {
        if (!isControlled) setInternalChecked(!value)
        onCheckedChange?.(!value)
      }}
      {...props}
    >
      <span
        className={cn(
          'pointer-events-none block h-5 w-5 rounded-full bg-background shadow-lg ring-0 transition-transform',
          value ? 'translate-x-5' : 'translate-x-0'
        )}
      />
    </button>
  )
})
Switch.displayName = 'Switch'

export { Switch }
